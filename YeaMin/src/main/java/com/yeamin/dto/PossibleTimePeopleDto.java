package com.yeamin.dto;

public class PossibleTimePeopleDto {
	
	private Integer possible_no;
	
	private String possible_dw;
	
	private String possible_time;
	
	private Integer possible_people;
	
	public PossibleTimePeopleDto() {
		
	}

	public Integer getPossible_no() {
		return possible_no;
	}

	public void setPossible_no(Integer possible_no) {
		this.possible_no = possible_no;
	}

	public String getPossible_dw() {
		return possible_dw;
	}

	public void setPossible_dw(String possible_dw) {
		this.possible_dw = possible_dw;
	}

	public String getPossible_time() {
		return possible_time;
	}

	public void setPossible_time(String possible_time) {
		this.possible_time = possible_time;
	}

	public Integer getPossible_people() {
		return possible_people;
	}

	public void setPossible_people(Integer possible_people) {
		this.possible_people = possible_people;
	}
	
}

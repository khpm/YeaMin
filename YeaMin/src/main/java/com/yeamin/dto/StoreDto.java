package com.yeamin.dto;

public class StoreDto {
	
	private String store_name;

	private String store_tel;

	private String store_addr;

	private String store_desc;

	private String store_week_business_time;

	private String store_weekend_business_time;

	public StoreDto() {
		
	}

	public String getStore_name() {
		return store_name;
	}

	public void setStore_name(String store_name) {
		this.store_name = store_name;
	}

	public String getStore_tel() {
		return store_tel;
	}

	public void setStore_tel(String store_tel) {
		this.store_tel = store_tel;
	}

	public String getStore_addr() {
		return store_addr;
	}

	public void setStore_addr(String store_addr) {
		this.store_addr = store_addr;
	}

	public String getStore_desc() {
		return store_desc;
	}

	public void setStore_desc(String store_desc) {
		this.store_desc = store_desc;
	}

	public String getStore_week_business_time() {
		return store_week_business_time;
	}

	public void setStore_week_business_time(String store_week_business_time) {
		this.store_week_business_time = store_week_business_time;
	}

	public String getStore_weekend_business_time() {
		return store_weekend_business_time;
	}

	public void setStore_weekend_business_time(String store_weekend_business_time) {
		this.store_weekend_business_time = store_weekend_business_time;
	}
	
}
